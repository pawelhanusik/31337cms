<h1>Articles</h1>
<p>In this tab you can create, edit and delete articles.</p>
<p>An article can be either of type:</p>
<ul>
<li>homepage,</li>
<li>internal.</li>
</ul>
<h3>Homepage articles</h3>
<p>They automatically show up on the homepage. If you wish, you are able to create a menu entry, which will scroll into the article.</p>
<h3>Internal articles</h3>
<p>They exists as a sub-site. You can create a menu entry, which will lead to them.</p>
<h2><span style="text-decoration: underline;">Article creation</span></h2>
<h4>Name</h4>
<p>It is used to identify an article in administrator panel. Moreover it is being used as HTML id for div in which the article will be placed. It shouldn't contains spaces.</p>
<h4>Contents</h4>
<p>TinyMCE editor is used to provide a friendly environment. For more advanced users, CodeMirror editor allows to write HTML code (PHP is allowed). TinyMCE is a default one, but editor can be changed dynamically, by pressing down buttons located below it.</p>
<h2>Note:</h2>
<p>After renaming an article or changing its type, old one doesn't get deleted. You have to delete it manually.</p>
