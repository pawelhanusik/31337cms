<h1>31337 cms</h1>
<p>is a database-less content management system with a feature of converting whole site into cool-looking 1337 site.</p>
<p>After logging into administrator panel, you'll see various tabs, which are described down below.</p>
<p>Once your site is fulfilled with content, feel free to customize it in a way you would do without any cms. Short description of files layout of final site can be found at the bottom of this site.</p>
<p>If you're sure, you won't use administrator panel anymore, you can freely delete admin directory, since it is not needed for site to function.</p>
