<h1>Customization</h1>
<p>There you can set various settings.</p>
<p>They are organized in 3 categories:</p>
<ul>
<li>general,</li>
<li>colors,</li>
<li>31337.</li>
</ul>
<p>First 2 categories are obvious, but last one seems mysterious.<br />There, in 31337 category, you can enable 1337 converter, which will convert normal text into 1337. It can be used for example as an April fool joke or just to make your site look cooler.</p>
<h2>Note:</h2>
<p>After changing header image type to slide show, you have to specify path to the first image. List of images, which will be added to slide show, will be gathered automatically. Just place them in the same directory as the first image.</p>
<p>Remember: if you want to add/delete images, you have to resave in order to generate new list of files used in the slide show.</p>
