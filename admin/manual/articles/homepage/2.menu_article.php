<h1>Menu</h1>
<p>In this tab you can create, edit and delete menu entries.</p>
<p>A menu entry can be one of these types:</p>
<ul>
<li>scroll into,</li>
<li>normal link,</li>
<li>external link,</li>
<li>homepage,</li>
<li>manual.</li>
</ul>
<h3>'Scroll into' menu entry</h3>
<p>is a link to a homepage article. If a user is on the homepage site, where the article is beeing displayed, the menu entry scrolls into the article, although if a user is not on the homepage site, the menu entry acts as a 'normal link' and changes site to one with the article.</p>
<h3>'Normal link' menu entry</h3>
<p>changes site to one with the article.</p>
<h3>'External link' menu entry</h3>
<p>opens new tab with a specified external URL.</p>
<h3>'Homepage' menu entry</h3>
<p>loads a homepage.</p>
<h3>'Manual' menu entry</h3>
<p>lets you enter custom HTML code (if you want, you can place there even a PHP code).<br />Each line is treated as a standalone menu entry, so you can add bunch of them at once.</p>
